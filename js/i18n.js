const DEFAULT_LANG = "en";
const LANG_STORAGE_KEY = "belagavi-organics-lang";

/* Get current language */
function getLang() {
  try {
    const saved = localStorage.getItem(LANG_STORAGE_KEY);
    if (saved && I18N[saved]) return saved;
  } catch (e) { /* ignore */ }
  // Browser language detection
  const browser = (navigator.language || "en").toLowerCase().split("-")[0];
  if (I18N[browser]) return browser;
  return DEFAULT_LANG;
}

/* Set language and apply translations */
function setLang(lang) {
  if (!I18N[lang]) lang = DEFAULT_LANG;
  const dict = I18N[lang];

  // Update <html> lang attribute
  document.documentElement.setAttribute("lang", LANG_META[lang].html_lang);
  document.documentElement.setAttribute("data-lang", lang);

  // Update all data-i18n elements (textContent)
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  // Update all data-i18n-html elements (innerHTML — supports <em> etc.)
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const key = el.getAttribute("data-i18n-html");
    if (dict[key]) {
      el.innerHTML = dict[key];
    }
  });

  // Persist
  try { localStorage.setItem(LANG_STORAGE_KEY, lang); } catch (e) {}

  // Update language button label
  const langCurrent = document.querySelector(".lang-current");
  if (langCurrent) langCurrent.textContent = LANG_META[lang].code;

  // Update active state in menu
  document.querySelectorAll(".lang-menu li").forEach(li => {
    li.classList.toggle("active", li.getAttribute("data-lang") === lang);
  });

  // Dispatch event so other scripts can react
  document.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
}

/* Initialize language on load */
document.addEventListener("DOMContentLoaded", () => {
  const lang = getLang();
  setLang(lang);
});

/* Expose on window so other scripts (products.js, main.js) can access */
window.I18N = I18N;
window.setLang = setLang;
window.getLang = getLang;
window.LANG_META = LANG_META;
